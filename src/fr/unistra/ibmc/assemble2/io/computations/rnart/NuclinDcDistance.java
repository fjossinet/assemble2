package fr.unistra.ibmc.assemble2.io.computations.rnart;

public class NuclinDcDistance {
    public int iq;
    public int j;
    public int x;
    public int iin;
    public int ind;
    public String ad1;
    public String ad2;
    public double d;
    public int idd1;
    public int idd2;

    public NuclinDcDistance(int a, int b, int c, int dd, int e, String f, String g, double h, int i, int jj) {
        iq = a;
        j = b;
        x = c;
        iin = dd;
        ind = e;
        ad1 = f;
        ad2 = g;
        d = h;
        idd1 = i;
        idd2 = jj;
    }
}
